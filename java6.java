// 6.How to swap 2 no using 3rd
//  variable Program
// in java
public class java6 {

    public static void main(String[] args) {
       int a = 60;
       int b = 85;
       int temp;

       temp = a;
       a = b ;
       b = temp;

       System.out.println("after swaping a is "+a);
       System.out.println("after swaping b is "+b);

    }
}
